const myElement1 = document.querySelector("#listimg1");
const otherElement1 = document.querySelector("#listtext1");


myElement1.addEventListener("mouseenter", function() {
  // otherElement 클래스 추가 코드
  listtext1.style.color = "#fa5887";
  text1.style.color = "#fa5887";

});

myElement1.addEventListener("mouseleave", function () {
  listtext1.style.color = "";
  text1.style.color = "";

});

